<template>
  <scroll ref="listview" 
        :data="data"
        @scroll="scroll"
        :listenScroll="listenScroll" 
        :probeType="probeType" 
        class="listview">
      <ul>
          <li ref="listGroup" class="list-group" v-for="group in data">
              <h1 class="list-group-title">{{group.title}}</h1>
              <ul>
                  <li @click="selectItem(item)" class="list-group-item" v-for="item in group.items">
                      <img :src="item.avata" class="avatar" alt="">
                      <span class="name">{{item.name}}</span>
                  </li>
              </ul>
          </li>
      </ul>
      <div class="shortcutList" 
            @touchstart="shortcutTouchStart"  
            @touchmove.stop.prevent="shortcutTouchMove">
          <ul>
              <li class="item" v-for="(item,index) in shortcutList" 
              :data-index="index"
              :class="{current:currentIndex===index}"
              >
                {{item}}
              </li>
          </ul>
      </div>
      <div class="list-fixed" v-show="fixedTitle" ref="fixed">
          <h1 class="fixed-title">{{fixedTitle}}</h1>
      </div>
      <div class="loading-container" v-show="!data.length">
         <loading/>
      </div>
  </scroll>
</template>
<script>
 import Scroll from 'base/scroll/scroll';
 import Loading from 'base/loading/loading';
 import {getData} from 'common/js/dom';
 const ITEM_LEN = 18;
 const TITLE_HEIGHT = 30;
export default {
  data(){
      return {
          scrollY:-1,
          currentIndex:0,
          diff:-1
      }
  },
  created(){
      this.touch={};
      this.listHeight=[];
      this.listenScroll = true;
      this.probeType = 3;
  },
  props:{
      data:{
          type:Array,
          default:[]
      }
  },
  computed:{
      shortcutList(){
        return  this.data.map(group => {
              return group.title.substr(0,1)
          })
      },
      fixedTitle(){
          if(this.scrollY>0){
              return '';
          }
          return this.data[this.currentIndex]?this.data[this.currentIndex].title:'';
      }
  },
  methods:{
      refresh(){
          this.$refs.listview.refresh()
      },
      shortcutTouchStart(e){
          let anchorIndex = getData(e.target,'index');
          let firstTouch = e.touches[0];
          this.touch.y1 = firstTouch.pageY;
          this.touch.anchorIndex = anchorIndex;
          this._scrollTo(anchorIndex);
      },
      shortcutTouchMove(e){
          let firstTouch = e.touches[0];
          this.touch.y2 = firstTouch.pageY;
          let delta = (this.touch.y2-this.touch.y1)/ITEM_LEN | 0
          let anchorIndex = delta+ parseInt(this.touch.anchorIndex);
          this._scrollTo(anchorIndex);

      },
      scroll(pos){
          this.scrollY = pos.y;
      },
      selectItem(item){
          this.$emit('select',item)
      },
       _calculateHeight(){
           const list = this.$refs.listGroup;
           let height = 0;
           this.listHeight.push(height);
           for(let i=0;i<list.length;i++){
               let item = list[i];
               height += item.clientHeight;
               this.listHeight.push(height);
           }
       },
      _scrollTo(index){
         if(!index&&index!==0){
             return;
         }
         if(index<0){
             index=0
         }else if(index>this.listHeight.length-2){
             index =this.listHeight.length;
         }
        this.scrollY = -this.listHeight[index];
        this.$refs.listview.scrollToElement(this.$refs.listGroup[index],0)
      }
  },
  watch:{
     data(){
         setTimeout(()=>{
             this. _calculateHeight()
         },20)
     },
     scrollY(newY){
         const listHeight = this.listHeight;
         //当滚动到最顶部
         if(newY > 0){
             this.currentIndex = 0;
             return;
         }
         //当在中间滚动
         for(var i=0;i<listHeight.length-1;i++){
             let height1 =listHeight[i];
             let height2 = listHeight[i+1];
             if(-newY >= height1&&-newY<height2){
                 this.currentIndex = i;
                 this.diff = height2+newY;
                 return;
             }
         }
             //当滚动到底部，且大于最后一个元素的上限    
             this.currentIndex = this.listHeight.length-2;
           
     },
     diff(newVal){
         let fixedTop = (newVal>0&&newVal<TITLE_HEIGHT)?newVal-TITLE_HEIGHT:0;
         if(this.fixedTop === fixedTop){
             return;
         }
         this.fixedTop = fixedTop;
         this.$refs.fixed.style.transform = `translate3d(0,${fixedTop}px,0)`;
     }
  },
  components:{
      Scroll,
      Loading
  }
}
</script>
